public class MiProgramaWhile {
    public static void main(String[] args) {
        
        // WHILE
        int energia = 3;

        System.out.println("Nivel de energia:");

        while (energia > 0) {
            System.out.println("Energia: " + energia);
            energia--;
        }

        System.out.println("Sin energia");
        
    }
}