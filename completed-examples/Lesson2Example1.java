public class MyClass {
    public static void main(String args[]) {
        // 1. int -> números enteros
        int edad = 15;
        
        // 2. double -> números con decimales
        double altura = 1.75;
        
        // 3. String -> cadenas de texto (entre comillas dobles)
        String nombre = "Ana";
        
        // 4. boolean -> valores lógicos (verdadero o falso)
        boolean estudia = true;
        
        // Enviamos las variables al flujo de salida estándar (consola)
        System.out.println("Nombre del estudiante: " + nombre);
        System.out.println("Edad: " + edad + " anos");
        System.out.println("Altura: " + altura + " metros");
        System.out.println("¿Esta estudiando?: " + estudia);
    }
}